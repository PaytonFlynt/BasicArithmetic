#include<iostream>

 //Start the main function
 int main() {

        //Declare and initialize
        int a = 10;
        int b = 5;

        //Arithmetic operations and display
        std::cout << a << "+" << b << "=" << (a + b) << std::endl;
        std::cout << a << "-" << b << "=" << (a - b) << std::endl;
        std::cout << a << "*" << b << "=" << (a * b) << std::endl;
        std::cout << a << "/" << b << "=" << (a / b) << std::endl;
        std::cout << a << "%" << b << "=" << (a % b) << std::endl;

        return 0;

    }
